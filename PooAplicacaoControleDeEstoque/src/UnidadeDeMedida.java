/**
 * Classe para manter a descrição das
 * uniades de medidas dos produtos
 */
public class UnidadeDeMedida {

    private Integer id;
    private String nome;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
}
